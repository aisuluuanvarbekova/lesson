package com.company;

public class GrandMother {

    private String name;
    private Hobby hobby;
    private Nationality nationality;

    public String getName() {
        return name;
    }

    public Hobby getHobby() {
        return hobby;
    }

    public Nationality getNationality() {
        return nationality;
    }

    public GrandMother(String name, Hobby hobby, Nationality nationality) {
        this.name = name;
        this.hobby = hobby;
        this.nationality = nationality;
    }
}




